package Service;

public class ObtenerException extends Exception{

	public ObtenerException() {
		super();
	}

	public ObtenerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ObtenerException(String message, Throwable cause) {
		super(message, cause);
	}

	public ObtenerException(String message) {
		super(message);
	}

	public ObtenerException(Throwable cause) {
		super(cause);
	
	}
	
	
}
