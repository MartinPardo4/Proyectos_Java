package Service;

public class EditarException extends Exception{

	public EditarException() {
		super();
	}

	public EditarException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EditarException(String message, Throwable cause) {
		super(message, cause);
	}

	public EditarException(String message) {
		super(message);
	}

	public EditarException(Throwable cause) {
		super(cause);
	}
	
	
}
