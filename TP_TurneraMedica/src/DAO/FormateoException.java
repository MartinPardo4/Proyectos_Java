package DAO;

public class FormateoException extends ConsultaException{

	public FormateoException() {
		super();
	}

	public FormateoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FormateoException(String message, Throwable cause) {
		super(message, cause);
	}

	public FormateoException(String message) {
		super(message);
	}

	public FormateoException(Throwable cause) {
		super(cause);
	}
	
	
}
