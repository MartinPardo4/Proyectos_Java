package DAO;

public class AbrirConexionException extends ConexionException{

	public AbrirConexionException() {
		super();
	}

	public AbrirConexionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AbrirConexionException(String message, Throwable cause) {
		super(message, cause);
	}

	public AbrirConexionException(String message) {
		super(message);
	}

	public AbrirConexionException(Throwable cause) {
		super(cause);
	}
	
	
}
