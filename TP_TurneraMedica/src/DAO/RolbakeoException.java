package DAO;

public class RolbakeoException extends EjecucionSentenciaException{

	public RolbakeoException() {
		super();
	}

	public RolbakeoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RolbakeoException(String message, Throwable cause) {
		super(message, cause);
	}

	public RolbakeoException(String message) {
		super(message);
	}

	public RolbakeoException(Throwable cause) {
		super(cause);
	}
	
	
}
