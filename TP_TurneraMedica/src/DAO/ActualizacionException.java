package DAO;

public class ActualizacionException extends EjecucionSentenciaException{

	public ActualizacionException() {
		super();
	}

	public ActualizacionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ActualizacionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ActualizacionException(String message) {
		super(message);
	}

	public ActualizacionException(Throwable cause) {
		super(cause);
	}
	
	
}
