package DAO;

public class EjecucionSentenciaException extends Exception{

	public EjecucionSentenciaException() {
		super();

	}

	public EjecucionSentenciaException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public EjecucionSentenciaException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public EjecucionSentenciaException(String message) {
		super(message);
		
	}

	public EjecucionSentenciaException(Throwable cause) {
		super(cause);
	
	}
	
	
}
