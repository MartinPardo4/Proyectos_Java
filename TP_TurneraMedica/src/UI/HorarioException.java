package UI;

public class HorarioException extends Exception{

	public HorarioException() {
		super();
	}

	public HorarioException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public HorarioException(String message, Throwable cause) {
		super(message, cause);
	}

	public HorarioException(String message) {
		super(message);
	}

	public HorarioException(Throwable cause) {
		super(cause);
	}

}
