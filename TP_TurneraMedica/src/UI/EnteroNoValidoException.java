package UI;

public class EnteroNoValidoException extends Exception{

	public EnteroNoValidoException() {
		super();
	}

	public EnteroNoValidoException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EnteroNoValidoException(String message, Throwable cause) {
		super(message, cause);
	}

	public EnteroNoValidoException(String message) {
		super(message);
	}

	public EnteroNoValidoException(Throwable cause) {
		super(cause);
	}
	
	
}
